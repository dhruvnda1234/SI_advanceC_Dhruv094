//Find the size of different datatypes (int,float,char,double) using sizeof.
#include<stdio.h>
int main()
{
    printf("\nInteger size = %d",sizeof(int));
    printf("\nFloat size = %d",sizeof(float));
    printf("\nChar size = %d",sizeof(char));
    printf("\nLong size = %d",sizeof(long));
    printf("\nDouble size = %d",sizeof(double));
    return 0;
}
